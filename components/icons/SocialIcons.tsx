// --- TYPE DEFINITIONS ---
export type SocialPlatform =
    | 'twitter' | 'farcaster' | 'github' | 'telegram' | 'discord'
    | 'website' | 'unknown';

// --- DETECTION LOGIC ---
export const detectPlatform = (url: string): SocialPlatform => {
    if (!url) return 'unknown';
    try {
        const hostname = new URL(url).hostname.replace('www.', '');
        if (hostname.includes('twitter.com') || hostname.includes('x.com')) return 'twitter';
        if (hostname.includes('warpcast.com')) return 'farcaster';
        if (hostname.includes('github.com')) return 'github';
        if (hostname.includes('t.me')) return 'telegram';
        if (hostname.includes('discord.gg') || hostname.includes('discord.com')) return 'discord';
        if (hostname.length > 0) return 'website';
    } catch (e) {
        return 'unknown';
    }
    return 'unknown';
};

// --- ICON MAPPING ---
export const getPlatformIcon = (platform: SocialPlatform): React.ReactNode => {
    switch (platform) {
        case 'twitter': return <TwitterIcon />;
        case 'farcaster': return <FarcasterIcon />;
        case 'github': return <GithubIcon />;
        case 'telegram': return <TelegramIcon />;
        case 'discord': return <DiscordIcon />;
        case 'website': return <GlobeIcon />;
        default: return <LinkIcon />;
    }
};

// --- UTILITY ---
export const extractHandle = (url: string): string => {
    if (!url) return '';
    try {
        const urlObj = new URL(url);
        const path = urlObj.pathname.split('/').filter(Boolean);
        const platform = detectPlatform(url);

        switch (platform) {
            case 'twitter':
            case 'github':
                return path[0] ? `@${path[0]}` : url;
            case 'farcaster':
                 return path[1] && path[0] === 'u' ? path[1] : (path[0] ? path[0] : url);
            case 'telegram':
                return path[0] ? `@${path[0]}` : url;
            default:
                return urlObj.hostname.replace('www.','');
        }
    } catch (e) {
        return url;
    }
}