// --- TYPE DEFINITIONS ---
export type SocialPlatform =
    | 'twitter' | 'lens' | 'github' | 'linkedin' | 'farcaster'
    | 'instagram' | 'facebook' | 'telegram' | 'discord' | 'youtube'
    | 'reddit' | 'twitch' | 'etherscan' | 'behance' | 'dribbble'
    | 'website' | 'unknown';

// --- DETECTION LOGIC ---
export const detectPlatform = (url: string): SocialPlatform => {
    if (!url) return 'unknown';
    try {
        const hostname = new URL(url).hostname.replace('www.', '');
        if (hostname.includes('twitter.com') || hostname.includes('x.com')) return 'twitter';
        if (hostname.includes('hey.xyz') || hostname.includes('lens.xyz')) return 'lens';
        if (hostname.includes('github.com')) return 'github';
        if (hostname.includes('linkedin.com')) return 'linkedin';
        if (hostname.includes('warpcast.com')) return 'farcaster';
        if (hostname.includes('instagram.com')) return 'instagram';
        if (hostname.includes('facebook.com')) return 'facebook';
        if (hostname.includes('t.me')) return 'telegram';
        if (hostname.includes('discord.gg') || hostname.includes('discord.com')) return 'discord';
        if (hostname.includes('youtube.com')) return 'youtube';
        if (hostname.includes('reddit.com')) return 'reddit';
        if (hostname.includes('twitch.tv')) return 'twitch';
        if (hostname.includes('etherscan.io')) return 'etherscan';
        if (hostname.includes('behance.net')) return 'behance';
        if (hostname.includes('dribbble.com')) return 'dribbble';
        // Add more specific domains before this generic one
        if (hostname.length > 0) return 'website';
    } catch (e) {
        return 'unknown';
    }
    return 'unknown';
};

