// --- UTILITY ---
export const extractHandle = (url: string): string => {
    if (!url) return '';
    try {
        const urlObj = new URL(url);
        const path = urlObj.pathname.split('/').filter(Boolean);
        const platform = detectPlatform(url);

        switch (platform) {
            case 'twitter':
            case 'github':
            case 'instagram':
            case 'twitch':
                return path[0] ? `@${path[0]}` : url;
            case 'lens':
            case 'farcaster':
                 return path[1] && path[0] === 'u' ? path[1] : (path[0] ? path[0] : url);
            case 'linkedin':
                 return path[1] && path[0] === 'in' ? path[1] : url;
            case 'telegram':
                return path[0] ? `@${path[0]}` : url;
            case 'reddit':
                return path[1] && path[0] === 'user' ? `u/${path[1]}` : url;
            case 'youtube':
                return path[0] && path[0].startsWith('@') ? path[0] : url;
            default:
                return urlObj.hostname.replace('www.','');
        }
    } catch (e) {
        return url;
    }
}
